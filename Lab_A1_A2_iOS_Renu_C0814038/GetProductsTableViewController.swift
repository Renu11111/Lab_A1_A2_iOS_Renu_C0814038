        let cell = tableView.dequeueReusableCell(withIdentifier: "cell", for: indexPath)

        cell.textLabel?.text =
            products[indexPath.row].productName
        cell.detailTextLabel?.text = products[indexPath.row].provider?.provider

        return cell
    }
    

