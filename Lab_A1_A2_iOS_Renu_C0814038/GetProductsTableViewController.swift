import UIKit
import CoreData

class GetProductsTableViewController: UITableViewController {
    var selectedProvider : Provider?
    var products : [Product] = []
    let context =
        (UIApplication.shared.delegate as! AppDelegate).persistentContainer.viewContext
    override func viewDidLoad() {
        super.viewDidLoad()
        if let _ = selectedProvider{
            let req : NSFetchRequest<Product> =  Product.fetchRequest()
            products = try! context.fetch(req)
            products = products.filter({$0.provider?.provider == selectedProvider?.provider})
            tableView.reloadData()
        }
    }

    // MARK: - Table view data source

    

    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        // #warning Incomplete implementation, return the number of rows
